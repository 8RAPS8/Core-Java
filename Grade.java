package in.co.rays1;

public class Grade {

	public static void main(String[] args) {
		int a=32;
		
		if(a>=90) {
			System.out.println("A+ Grade");
		}
		else if (a>=85) {
			System.out.println("A Grade");
		}
		else if (a>=80) {
			System.out.println("B+ Grade");
		}
		else if (a>=75) {
			System.out.println("B Grade");
		}
		else if (a>=70) {
			System.out.println("C+ Grade");
		}
		else if (a>=65) {
			System.out.println("C Grade");
		}
		else if (a>=55) {
			System.out.println("D+ Grade");
		}
		else if (a>=45) {
			System.out.println("D Grade");
		}
		else if (a>=33) {
			System.out.println("E Grade");
		}
		else if (a<33) {
			System.out.println("Fail");
		}
	
	}

}
