package in.co.rays1;

public class Concatenation {

	public static void main(String[] args) {
		String firstName="Vijay";
		String lastName="Chauhan";
		System.out.println("Concatenation:"+firstName.concat(lastName));
		String firstName1="Rahul";
		String lastName1="Patel";
		System.out.println("Concatenation:"+firstName1.concat(lastName1));
	}

}
